"""Pure text-emit helpers — import blocks, docstrings, field lines.

No spec knowledge; just turns already-computed specs into source lines. Files are run
through ``ruff format`` afterwards, so spacing here only needs to be valid, not perfect.
"""

from __future__ import annotations

from collections.abc import Iterable

GENERATED_HEADER = "# AUTO-GENERATED by avitoapi.codegen — regenerate, do not hand-edit.\n"

#: token → import line for the stdlib/house symbols the mapper may emit.
_TYPE_IMPORTS = {
    "datetime": "from datetime import datetime",
    "date": "from datetime import date",
    "TZDatetime": "from .common import TZDatetime",
    "TZDatetime_method": "from ..models.common import TZDatetime",
}


def module_doc(title: str, kind: str, *, see: str | None = None) -> str:
    body = f"{title} — {kind} (auto-generated from the Avito OpenAPI spec)."
    if see:
        body += f"\n\nSee: {see}"
    return f'"""{body}"""\n'


def docstring(lines: Iterable[str], indent: str = "    ") -> str:
    body = [ln for ln in lines if ln]
    if not body:
        return ""
    if len(body) == 1:
        return f'{indent}"""{body[0]}"""\n'
    out = f'{indent}"""{body[0]}\n\n'
    for ln in body[1:]:
        out += f"{indent}{ln}\n"
    return out + f'{indent}"""\n'


def class_docstring(
    summary: str,
    section: str,
    items: Iterable[tuple[str, str | None]],
    indent: str = "    ",
    note: str | None = None,
) -> str:
    """Google-style class docstring: summary, an optional ``note`` line, then a section.

    Field descriptions live here (per project convention), never in ``Field(..., description=…)``.
    ``note`` renders one line under the summary — used for the Avito docs link.
    """

    documented = [(n, d) for n, d in items if d]
    if not documented and not note:
        return f'{indent}"""{summary}"""\n'
    out = f'{indent}"""{summary}\n'
    if note:
        out += f"\n{indent}{note}\n"
    if documented:
        out += f"\n{indent}{section}:\n"
        for name, desc in documented:
            out += f"{indent}    {name}: {desc}\n"
    return out + f'{indent}"""\n'


def datetime_imports(needs: set[str], *, in_methods: bool) -> list[str]:
    """Return import lines for the datetime/house type tokens actually used."""

    out: list[str] = []
    if "datetime" in needs:
        out.append(_TYPE_IMPORTS["datetime"])
    if "date" in needs:
        out.append(_TYPE_IMPORTS["date"])
    if "TZDatetime" in needs:
        out.append(_TYPE_IMPORTS["TZDatetime_method"] if in_methods else _TYPE_IMPORTS["TZDatetime"])
    return out


def field_line(name: str, annotation: str, assignment: str | None) -> str:
    """Render one field line — bare (``name: T``) when ``assignment`` is ``None``."""

    if assignment is None:
        return f"    {name}: {annotation}\n"
    return f"    {name}: {annotation} = {assignment}\n"
